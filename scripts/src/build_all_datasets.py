"""Standard entry point for the public debt pipeline.

The repository keeps configuration in scripts/config.py and exposes the run from
scripts/src so the structure is consistent across repositories. The existing
source-specific modules are loaded from scripts/ for backward compatibility.

This entry point patches table writers before the legacy modules are imported.
Every CSV generated by the pipeline is therefore saved with a JSON records
companion using the same file name base.
"""

import importlib.util
import sys
from pathlib import Path

import pandas as pd

REPO_ROOT = Path(__file__).resolve().parents[2]
SCRIPTS_DIR = REPO_ROOT / "scripts"

if str(SCRIPTS_DIR) not in sys.path:
    sys.path.insert(0, str(SCRIPTS_DIR))


def write_dataframe_json(df, path):
    """Save a DataFrame in JSON records format."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    if df is None or df.empty:
        path.write_text("[]\n", encoding="utf-8")
    else:
        df.to_json(path, orient="records", force_ascii=False, indent=2)
        path.write_text(path.read_text(encoding="utf-8") + "\n", encoding="utf-8")
    return path


def write_csv_with_json(df, path):
    """Save a DataFrame as CSV and create the JSON records companion."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    if df is None:
        df = pd.DataFrame()
    df.to_csv(path, index=False, encoding="utf-8")
    write_dataframe_json(df, path.with_suffix(".json"))
    return path


def write_csv_if_not_empty_with_json(df, path):
    """Write final CSV output and the matching JSON records file."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    if df is None or df.empty:
        path.write_text("", encoding="utf-8")
        write_dataframe_json(pd.DataFrame(), path.with_suffix(".json"))
    else:
        df.to_csv(path, index=False, encoding="utf-8")
        write_dataframe_json(df, path.with_suffix(".json"))
    return path


def patch_output_writers():
    """Patch shared writer functions before loading the pipeline modules."""
    import io_utils
    import normalization_utils

    io_utils.write_csv = write_csv_with_json
    normalization_utils.write_csv_if_not_empty = write_csv_if_not_empty_with_json


def load_legacy_pipeline_module():
    """Load the existing root pipeline module under a private module name."""
    module_path = SCRIPTS_DIR / "build_all_datasets.py"
    spec = importlib.util.spec_from_file_location("legacy_build_all_datasets", module_path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def main():
    """Run the full pipeline with CSV and JSON output enabled."""
    patch_output_writers()
    module = load_legacy_pipeline_module()
    module.main()


if __name__ == "__main__":
    main()
